#include <stdlib.h>
#include <unistd.h>
#include <sys/wait.h>

int		main(void) {
	if (fork() == 0) {
			write(1, "child\n", 6);
	} else {
			wait(0);
			write(1, "parent\n", 7);
	}
	exit(0);
}
